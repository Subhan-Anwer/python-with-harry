# there are mainly 5 data types in python

# 1. Intergers ( int )
# it includes whole numbers, both positive and negative, without decimals.
# EXample: 1, 2, 3, 0, -1, -2, -3
a = 1


# 2. Float ( float )
# it includes decimal numbers, both positive and negative.
# Example: 1.4, 2.558, -3.4, 0.9, -1.2, 6.9
b = 69.578

# 3. String ( str )
# it includes a sequence of characters enclosed in single or double quotes.
# Example: "Subhan sheikh", 'Noman', "Hello World", 'Python', "123", '456'
name = "Subhan Sheikh"

# 4. Boolean ( bool )
# it includes True or False values.
# Example: 
subhanIsStudent = True

# 5. None ( None )
# it includes a null value or no value at all.
# Example: 
bankBalance = None