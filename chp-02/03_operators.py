# Operators

# Operators are special symbols in Python that perform operations on variables and values.
# Following are some common types of operators in Python:

# Arithmetic Operators
5 + 2  # + is an addition operator that adds two numbers
7 - 2  # - is an subtraction operator that subtracts two numbers
5 * 2  # * is an multiplication operator that multiplies two numbers
6 / 2  # / is an division operator that divides two numbers
7 % 2  # % is an modulus operator that returns the remainder of a division operation
3 ** 3 # ** is an exponentiation operator that raises a number to the power of another number
9 // 3 # // is an floor division operator that divides two numbers and returns the largest integer less than or equal to the result

# Assignment operators
a = 5 # = is assignment operator that assigns the value on the right to the variable on the left
a += 2 # += is an increment assignment operator that adds the right operand to the left operand and assigns the result to the left operand
a -= 2 # -= is a decrement assignment operator that subtracts the right operand from the left operand and assigns the result to the left operand

